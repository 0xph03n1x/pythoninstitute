#!/usr/bin/python3

a = float(input("Enter first number: ")) # input a float value for variable a here
b = float(input("Enter second number: ")) # input a float value for variable b here

print("Adding the numbers: " + str(a + b)) # output the result of addition here
print("Subtracting the numbers: " + str(a - b)) # output the result of subtraction here
print("Multiplying the numbers: " + str(a * b)) # output the result of multiplication here
print("Dividing the numbers: " + str(a / b)) # output the result of division here

print("\nThat's all, folks!")
